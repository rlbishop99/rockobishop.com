        egg that was trying to "eggscape" the chicken coop out of fear of being eaten. The egg moves with a physics body and moves kinetically through impulses.
        It was the first Game Jam I had ever participated in, and it was the first time I had made a full game that was playable from start to finish.
        I certainly learned a lot while making it, it taught me a lot about debugging, code review, creativity, and good code optimization practices.
